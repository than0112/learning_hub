//輸入 某一正整數
//輸出 <=某一正整數的所有畢氏三元數及其組數
#include <iostream>
#include <cmath>
using namespace std;
int main(){
    // 畢氏三元數是指滿足 a^2 + b^2 = c^2 的整數三元組 (a, b, c)
    // 例如 (3, 4, 5) 是一組畢氏三元數，因為 3^2 + 4^2 = 5^2
    // 我們需要找出所有滿足條件的三元組 (a, b, c)，其中 a, b, c 都是正整數，且 a <= b < c <= n
    // 輸入一個正整數 n，輸出所有的畢氏三元數及其組數
    // 使用三重迴圈來遍歷所有可能的 a, b, c
    //在這邊假設total=0是畢氏三元數的組數
    int n,a,b,c,total=0;
    cout<<"請輸入一個正整數：";
    cin>>n;
    // 使用三重迴圈來遍歷所有可能的 a, b, c
    for (a = 1; a <= n; a++) {
        for(b=1;b<=n;b++){
            for(c=1;c<=n;c++){
                // 檢查是否滿足畢氏定理
                if (a * a + b * b == c * c ) {
                    // 如果滿足條件，則輸出這組畢氏三元數
                    if(a<b &&b<c){
                        cout << "(" << a << ", " << b << ", " << c << ")" << endl;
                        total++; // 增加組數計數
                    }
                }
            }
        }

    }
    // 輸出總組數
    cout<<"小於等於"<<n<<"的畢氏三元數組數為：" << total <<"組"<< endl;
    return 0;
}
